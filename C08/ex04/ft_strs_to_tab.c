#include <stdlib.h>
#include "ft_stock_str.h"

int					ft_strlen(char *str)
{
	int i;

	i = 0;
	while (str[i])
		i++;
	return (i);
}

char				*ft_strdup(char *src)
{
	int		slen;
	int		i;
	char	*dest;

	i = 0;
	slen = ft_strlen(src) + 1;
	dest = (char*)malloc(slen * sizeof(char));
	if (!dest)
		return (0);
	while (i < slen)
	{
		dest[i] = src[i];
		i++;
	}
	return (dest);
}

struct s_stock_str	*ft_strs_to_tab(int ac, char **av)
{
	int					i;
	struct s_stock_str	*t;

	i = 0;
	t = (struct s_stock_str *)malloc((ac + 1) * (sizeof(struct s_stock_str)));
	if (!t)
		return (0);
	while (i < ac)
	{
		t[i].size = ft_strlen(av[i]);
		t[i].str = av[i];
		t[i].copy = ft_strdup(av[i]);
		i++;
	}
	t[i].str = 0;
	return (t);
}
