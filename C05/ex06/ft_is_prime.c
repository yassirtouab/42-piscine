int		ft_is_prime(int nb)
{
	int x;

	x = 3;
	if (nb == 0 || nb == 1 || nb < 0)
		return (0);
	if (nb == 2)
		return (1);
	while (x < nb - 1)
	{
		if (nb % x == 0)
			return (0);
		x++;
	}
	return (1);
}
