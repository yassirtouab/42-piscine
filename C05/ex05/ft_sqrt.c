int		ft_sqrt(int nb)
{
	int x;

	x = 1;
	while (x <= nb / 2)
	{
		if (nb == x * x)
			return (x);
		else if (x > 46340)
			return (0);
		x++;
	}
	if (nb != x * x)
		return (0);
	return (x);
}
